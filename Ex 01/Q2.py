# 2.Relational Operator Demo
# Accept two numbers and display the result of all relational operations
# (==, !=, >, <, >=, <=) in a formatted way.

a,b = map(int, input("Enter two numbers: ").split())

print("------Relational Operator Demo------")
print("a == b: ", a==b)
print("a != b: ", a!=b)
print("a > b: ", a>b)
print("a < b: ", a<b)
print("a >= b: ", a>=b)
print("a <= b: ", a<=b)
print("-----------------------------------------")
